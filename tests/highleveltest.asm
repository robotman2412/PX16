
// region text
	// IRQ vector.
	.db irqv
	// NMI vector.
	.db nmiv
	// Entry vector.
	.db entry



	// IRQ Handler.
irqv:
	MOV PF, [ST]
	MOV PC, [ST]



	// NMI Handler.
nmiv:
	MOV PF, [ST]
	MOV PC, [ST]



	// Code entrypoint.
entry:
	// Generic initialiser.
	MOV ST, 0xffff
	SUB ST, [0xffff]
	OR  PF, 0x0003
	
	// Show sumthin.
	LEA R1, [PC~img_logo0]
	LEA R2, [PC~img_logo]
.loop:
	MOV R0, R1
	LEA.JSR PC, [PC~disp_img]
	LEA.JSR PC, [PC~wait]
	ADD R1, 32
	CMP R1, R2
	LEA.ULE PC, [PC~.loop]
	
	// Ded.
	DEC PC




	// Waits for a bit.
wait: // region
	MOV [ST], R0
	MOV R0, 0x1000
.loop:
	DEC R0
	LEA.CS PC, [PC~.loop]
	MOV R0, [ST]
	MOV PC, [ST]
	// endregion



	// Simple helper that copies source to the screen.
	// Parameters: src.
disp_img: // region
	// Function entry.
	MOV [ST], R2
	MOV [ST], R1
	
	// Subroutine call: memcpy(0xffc0, src, 32)
	// Set limit to 32 (the size of the screen).
	MOV R2, 32
	// Move over the source to the proper register.
	MOV R1, R0
	// Set dest to 0xffc0 (the address of the screen).
	MOV R0, 0xffc0
	// Jump to subroutine.
	LEA.JSR PC, [PC~memcpy]
	
	// Return.
	MOV R1, [ST]
	MOV R2, [ST]
	MOV PC, [ST]
	// endregion



	// Copies a region of memory to another.
	// Does not check for overlapping memory.
	// Parameters: dst, src, size.
memcpy: // region
	// Function entry.
	MOV [ST], R3
	
	// Limit prep and check size > 0.
	DEC R2
	LEA.ULT PC, [PC~.exit]
	
	// Copying loop.
.loop:
	// Loads from the source.
	MOV R3, [R1+R2]
	// Stores to the destination.
	MOV [R0+R2], R3
	// Decrements the limit.
	DEC R2
	// Loop condition.
	LEA.UGE PC, [PC~.loop]
	
	// Return.
.exit:
	MOV R3, [ST]
	MOV PC, [ST]
	// endregion


//endregion text.



